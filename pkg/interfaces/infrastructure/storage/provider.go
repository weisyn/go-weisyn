// Package storage provides storage provider interfaces.
package storage

// Package storage 提供WES系统的统一存储提供者接口定义
//
// 💾 **存储提供者管理 (Storage Provider Management)**
//
// 本文件定义了WES系统的统一存储访问接口，专注于：
// - 多种存储后端的统一管理
// - 存储实例的创建和配置
// - 存储类型的动态选择和切换
// - 存储资源的生命周期管理
//
// 🎯 **设计原则**
// - 统一访问：为所有存储需求提供统一的访问入口
// - 类型安全：强类型的存储接口，避免运行时错误
// - 灵活配置：支持多种存储后端的灵活配置
// - 性能优化：针对不同存储类型的性能优化

import "github.com/weisyn/v1/pkg/types"

//=============================================================================
// Provider 接口定义
//=============================================================================

// Provider 存储提供者接口
// 作为所有存储模型的统一访问点
// 管理和提供对不同类型存储的访问能力
type Provider interface {
	//-------------------------------------------------------------------------
	// 存储实例获取
	//-------------------------------------------------------------------------

	// GetBadgerStore 获取BadgerDB键值存储
	// name: 存储实例名称，用于区分不同用途的存储
	// 返回指定名称的键值存储实例
	// 如果实例不存在，返回错误
	GetBadgerStore(name string) (BadgerStore, error)

	// GetMemoryStore 获取内存存储
	// name: 存储实例名称，用于区分不同用途的存储
	// 返回指定名称的内存存储实例
	// 如果实例不存在，返回错误
	GetMemoryStore(name string) (MemoryStore, error)

	// GetFileStore 获取文件存储
	// name: 存储实例名称，用于区分不同用途的存储
	// 返回指定名称的文件存储实例
	// 如果实例不存在，返回错误
	GetFileStore(name string) (FileStore, error)

	// GetTempStore 获取临时存储
	// name: 存储实例名称，用于区分不同用途的存储
	// 返回指定名称的临时存储实例
	// 如果实例不存在，返回错误
	GetTempStore(name string) (TempStore, error)

	//-------------------------------------------------------------------------
	// 生命周期管理
	//-------------------------------------------------------------------------

	// Close 关闭所有存储连接
	// 注意：存储资源由DI容器自动管理，无需手动Close()
}

//=============================================================================
// ProviderOptions 结构体定义
//=============================================================================

// 兼容别名（数据结构迁至 pkg/types）
type ProviderOptions = types.ProviderOptions
